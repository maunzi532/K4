package k4.plane;

public class PlaneFrame
{
	public final int startY;
	public final int startX;
	public final int endY;
	public final int endX;

	public PlaneFrame(int startY, int startX, int endY, int endX)
	{
		this.startY = startY;
		this.startX = startX;
		this.endY = endY;
		this.endX = endX;
	}
}